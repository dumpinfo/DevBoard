	ds1820_reset();
	ds1820_write_byte(0xCC);	//勿略地址
	ds1820_write_byte(0xBE);	//读取温度
	for (i = 0; i < 9; i++) 
        {
		buf[i] = ds1820_read_byte();
		
        }
	i = buf[1];
	i <<= 8;
	i |= buf[0];
	temp_value=i;
        temp_value=temp_value*0.625;    //不是乘以0.0625的原因是为了把小数点后一位数据也转化为可以显示的数据
