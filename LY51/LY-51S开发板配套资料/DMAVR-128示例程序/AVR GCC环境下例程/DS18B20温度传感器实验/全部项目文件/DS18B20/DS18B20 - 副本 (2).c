//************************************************************************* 
//			DS18B20初始化
//*************************************************************************

unsigned char ds1820_reset(void)                 //初始化和复位

{
unsigned char i;
        DQ_OUT;
	DQ_CLR;
	delay_us(500);				//延时500uS(480-960)
	DQ_SET;
	DQ_IN;
	delay_us(80);				//延时80uS
	i = DQ_R;
	delay_us(500);				//延时500uS(保持>480uS)
	
	if (i) 
        {
		return 0x00;
		
        }
	else 
        {
		return 0x01;
		
        }

}


//************************************************************************* 
//			DS18B20读一个字节函数
//************************************************************************* 
   
unsigned char ds1820_read_byte(void) 
{

	unsigned char i;
	unsigned char value = 0;
	for (i = 8; i != 0; i--) 
        {
		value >>= 1;
		DQ_OUT;
		DQ_CLR;
                delay_us(4);				//*延时4uS
		
		DQ_SET;
		DQ_IN;
                delay_us(10);				//*延时10uS
		
		if (DQ_R) 
                {
			value|=0x80;
			
                }
		delay_us(60);			       //*延时60uS
		
        }
	
	return(value);
}

//************************************************************************* 
//			向18B20写一个字节函数
//*************************************************************************  

/*DS18B20字节写入函数*/
void ds1820_write_byte(unsigned char value) 
{

	unsigned char i;
	for (i = 8; i != 0; i--) 
        {
		DQ_OUT;
		DQ_CLR;
                delay_us(4);			//延时4uS
		if (value & 0x01) 
                {
			DQ_SET;
			
                }
		delay_us(80);			//延时80uS
		DQ_SET;			        //位结束
		value >>= 1;
		
        }
	
}
//*************************************************************************
//				发送温度转换命令
//************************************************************************* 

/*启动ds1820转换*/
void ds1820_start(void) {

	ds1820_reset();
	ds1820_write_byte(0xCC);	//勿略地址
	ds1820_write_byte(0x44);	//启动转换
}

